# ============================================================
# 问题类型识别
# 通过关键词匹配来判断用户的问题属于哪个类别
# ============================================================

# 定义每种问题类型的关键词列表
# 这是一个"字典套列表"的结构:
# - 外层是字典，key是问题类型，value是关键词列表
# - 内层是列表，存放该类型对应的所有关键词
QUESTION_TYPE_KEYWORDS = {
    "姻缘": [
        "姻缘", "爱情", "感情", "恋爱", "对象", "喜欢",
        "暗恋", "分手", "复合", "婚姻", "桃花", "另一半"
    ],
    "学业": [
        "学业", "考试", "学习", "成绩", "大学", "考研",
        "高考", "论文", "毕业", "课程", "专业"
    ],
    "事业": [
        "事业", "工作", "职业", "升职", "跳槽", "面试",
        "创业", "公司", "领导", "同事"
    ],
    "财运": [
        "财运", "钱", "财富", "投资", "理财", "收入",
        "工资", "赚钱", "买房", "买车"
    ],
    "健康": [
        "健康", "身体", "生病", "医院", "运动", "减肥",
        "睡眠", "精神", "心理"
    ],
}


def detect_question_type(question):
    """
    识别问题类型

    通过检查问题中的关键词，判断用户问的是哪方面的问题
    例如: "我什么时候能找到对象" -> 匹配到"对象" -> 返回"姻缘"

    参数:
        question - 用户的问题文字

    返回值:
        问题类型的字符串，如 '姻缘'、'学业' 等
        如果没有匹配到任何关键词，返回 '综合运势'
    """
    # 遍历每种问题类型
    for q_type, keywords in QUESTION_TYPE_KEYWORDS.items():
        # 遍历该类型的所有关键词
        for keyword in keywords:
            # 检查问题中是否包含这个关键词
            if keyword in question:
                return q_type

    # 没有匹配到任何关键词，返回默认类型
    return "综合运势"


