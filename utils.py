def format_cards_text(cards):
    """
    将牌面数据格式化为文字描述

    把牌面的JSON数据转换成AI容易理解的文字格式

    参数:
        cards - 牌面数据列表，每个牌是一个字典

    返回值:
        格式化后的文字(字符串)

    示例输出:
        第1张牌: 愚者(The Fool) - 正位
        关键词: 新开始, 冒险, 天真, 自由
        含义: 代表新的开始，充满无限可能...

        第2张牌: 恋人(The Lovers) - 逆位
        关键词: 不和谐, 价值观冲突
        含义: 内在的冲突导致了不和谐...
    """
    # 正位/逆位的中文映射
    positions_cn = {"upright": "正位", "reversed": "逆位"}

    card_text = ""
    for i, card in enumerate(cards, 1):  # enumerate从1开始计数
        pos_cn = positions_cn.get(card["position"], "正位")
        card_text += f"\n第{i}张牌: {card['name_cn']}({card['name_en']}) - {pos_cn}\n"
        card_text += f"关键词: {', '.join(card['keywords'])}\n"
        card_text += f"含义: {card['meaning']}\n"

    return card_text


def format_cards_for_followup(cards):
    """
    将牌面数据格式化为简短文字(用于追问时的上下文)

    追问时不需要完整的牌面信息，只需要简短的牌名和正逆位

    参数:
        cards - 牌面数据列表

    返回值:
        简短的文字描述
    """
    positions_cn = {"upright": "正位", "reversed": "逆位"}
    parts = []

    for card in cards:
        pos_cn = positions_cn.get(card.get("position"), "正位")
        name_en = card.get("name_en", "")
        parts.append(f"{card['name_cn']}({name_en}) - {pos_cn}")

    return ", ".join(parts)
